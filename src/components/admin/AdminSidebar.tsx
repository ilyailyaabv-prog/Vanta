/* ── Sidebar Component ── */

interface AdminSidebarProps {
  open: boolean;
  onClose: () => void;
  user?: {
    name?: string | null;
    email: string;
    role: string;
  };
}

export function AdminSidebar({ open, onClose, user }: AdminSidebarProps) {
  const pathname = usePathname();

  return (
    <>
      {/* Mobile backdrop */}
      {open && (
        <div
          className="fixed inset-0 z-40 bg-black/60 backdrop-blur-sm md:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={cn(
          "fixed inset-y-0 left-0 z-50 flex w-64 flex-col border-r border-border bg-sidebar transition-transform duration-300 md:static md:translate-x-0",
          open ? "translate-x-0" : "-translate-x-full",
        )}
      >
        {/* Logo area */}
        <div className="flex h-16 items-center justify-between border-b border-border px-6">
          <Link href="/admin" className="flex items-center gap-2">
            <span className="text-xl font-bold tracking-tight text-foreground">
              Vanta
            </span>
            <span className="h-2 w-2 rounded-full bg-copper" />
          </Link>
          <button
            type="button"
            className="text-muted-foreground hover:text-foreground md:hidden"
            onClick={onClose}
            aria-label="Close sidebar"
          >
            <CloseIcon />
          </button>
        </div>

        {/* Navigation links */}
        <nav className="flex-1 overflow-y-auto px-3 py-4">
          <ul className="space-y-1">
            {navItems.map(({ label, href, icon: Icon }) => {
              const isActive = pathname === href;
              return (
                <li key={href}>
                  <Link
                    href={href}
                    onClick={onClose}
                    className={cn(
                      "group flex items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium transition-colors",
                      isActive
                        ? "border-l-2 border-copper bg-sidebar-accent text-copper-light"
                        : "text-sidebar-foreground hover:bg-sidebar-accent hover:text-foreground",
                    )}
                  >
                    <Icon
                      className={cn(
                        "shrink-0",
                        isActive ? "text-copper" : "text-muted-foreground group-hover:text-foreground",
                      )}
                    />
                    {label}
                  </Link>
                </li>
              );
            })}
          </ul>
        </nav>

        {/* Logout */}
        <div className="border-t border-border px-3 py-4">
          <div className="mb-2 px-3 py-2">
            <p className="truncate text-xs text-muted-foreground">
              {user?.email ?? ""}
            </p>
            <p className="truncate text-xs font-medium text-sidebar-foreground capitalize">
              {user?.role ?? ""}
            </p>
          </div>
          <form action="/api/auth/signout" method="POST">
            <button
              type="submit"
              className="flex w-full items-center gap-3 rounded-lg px-3 py-2.5 text-sm font-medium text-muted-foreground transition-colors hover:bg-sidebar-accent hover:text-foreground"
            >
              <LogOutIcon className="shrink-0" />
              Log out
            </button>
          </form>
        </div>
      </aside>
    </>
  );
}